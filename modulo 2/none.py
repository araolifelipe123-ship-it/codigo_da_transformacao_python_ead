# Exibindo uma mensagem na tela
print("Olá! Testando o interpretador Python.")

# Identificando diferentes tipos de dados
print(type("Olá Mundo"))  # Retorna <class 'str'> (Texto)
print(type(25))           # Retorna <class 'int'> (Número Inteiro)
print(type(4.5))          # Retorna <class 'float'> (Número Decimal)
print(type(True))         # Retorna <class 'bool'> (Booleano)